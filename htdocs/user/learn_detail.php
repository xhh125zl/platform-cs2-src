    	<h3>网中网释疑：微信公众平台关于处理返利返现欺诈行为的公告</h3>
        <h5>2016-09-21</h5>
        <div class="image_x"><img src="http://img4.imgtn.bdimg.com/it/u=3806722984,3770116870&fm=21&gp=0.jpg"></div>
        <p>
        	微信公告内容如下：<br><br>
        近 期，平台发现有用户利用微信关系链，通过微信公众帐号、微信支付实施高额返现返利行为。此行为模式多数表现为消费佣金返现返利、多级多层返现返利等（违规 示例如下），一定程度上体现了金字塔欺诈、庞氏骗局等行为特征。其本质上是欺诈行为，即以高额返现返利吸引用户参与、以新入用户资金来支付原有用户的返现 返利，形成层压式资金链条。<br><br>
        对于上述欺诈行为，平台近期收到大量用户的投诉。为了维护广大用户的利益，维护公众平台的良好生态，依照《微信公众平台服务协议》、《微信公众平台运营规范》，一经发现存在此类行为的帐号，微信公众平台将对其永久封号处理，并有权拒绝再向该运营主体提供服务。<br><br>
        群众遇到此类欺诈行为，应当第一时间通过法律途径，向户籍地或涉嫌诈骗商户所在地公安机关报案，腾讯将积极协助公安机关依法处理。<br><br>
        网中网科技将提倡公告内容，严禁商家一欺诈、骗局形式发展分销，一经发现，将第一时间终止与商家的合作关系，不提供任何帮助。同时，网中网科技将有责任和义务引导商家开展正规分销，提倡合理合法的微信分销。
        微 信商城的分销属于二次支付结算，属于网上的黄牛做事情，属于不合法的金融法规。因为微信支付是属于一级结算机构，钱直接由微信支付，传到商家的钱是合法 的，但是商家需要二次分发结算到帮助推广三级分销的佣金提成，这个属于依附于微信支付的二清结算机构，这属于非法形式运营金融。二次支付结算并没有支付牌 照，只有微信支付才有支付牌照，属于合法。<br><br>
        二次支付结算不合法，即通过微信公众帐号、微信支付实施高额返现返利行为不合法。微信团队此次公告封杀的是结算方式不合法的微信公众平台，而并非指分销、分销模式、三级分销、分销商城不合法，更不会封杀分销、分销模式、三级分销、分销商城。<br><br>
        通过其他的合法支付方式、返利方式，即可继续开展微商城分销活动。<br><br>
        使用网中网科技的产品好分销微信分销系统的商家，至今并未反馈任何封号信息，请广大商家放心使用。
